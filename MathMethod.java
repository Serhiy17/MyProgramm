
public class MathMethod {
    public static void main(String[] args) {

        System.out.println("Math abs is " + Math.abs(23.7));
        System.out.println("Math abs is " + Math.abs(0.0));
        System.out.println("Math abs is " + Math.abs(-23.7));
        System.out.println("Math ceil is " + Math.ceil(9.2));
        System.out.println("Math ceil is " + Math.ceil(-9.8));
        System.out.println("Math cos is " + Math.cos(0.0));
        System.out.println("Math exp is " + Math.exp(1.0));
        System.out.println("Math exp is " + Math.exp(2.0));
        System.out.println("Math floor is " + Math.floor(9.2));
        System.out.println("Math floor is " + Math.floor(-9.8));
        System.out.println("Math natural logarithm is " + Math.log(Math.E));
        System.out.println("Math natural logarithm is " + Math.log(Math.E * Math.E));
        System.out.println("Math max is " + Math.max(2.3, 12.7));
        System.out.println("Math max is " + Math.max(-2.3, -12.7));
        System.out.println("Math min is " + Math.min(2.3, 12.7));
        System.out.println("Math min is " + Math.min(-2.3, -12.7));
        System.out.println("Math pow is " + Math.pow(2.0, 7.0));
        System.out.println("Math pow is " + Math.pow(9.0, 0.5));
        System.out.println("Math sin is " + Math.sin(0.0));
        System.out.println("Math sqrt is " + Math.sqrt(900.0));
        System.out.println("Math tan is " + Math.tan(0.0));

    }
}
